function menuOn() {
	var menuMobile = document.getElementById('mobile_animation');
	var pos = -250;
	var id = setInterval(frame, 10);
	function frame() {
		if (pos == 0) {
			clearInterval(id);
		}
		else{
			pos+= 10;
			menuMobile.style.left = pos + "px ";
		}
	}
}

function menuOff() {
	var menuMobile = document.getElementById('mobile_animation');
	var pos = 0;
	var id = setInterval(frame, 10);
	function frame() {
		if (pos == -250) {
			clearInterval(id);
		}
		else{
			pos-= 10;
			menuMobile.style.left = pos + "px ";
		}
	}
}